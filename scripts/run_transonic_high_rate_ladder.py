"""Adaptive high-rate continuation audit for the transonic homotopy branch."""

from __future__ import annotations

from pathlib import Path

import numpy as np

from imri_qpe.layer3_minidisk_1d import (
    TransonicSlimParams,
    remap_profile_to_new_sonic_grid,
    solve_low_mdot_transonic_homotopy,
)
from imri_qpe.parameters import FiducialParams
from imri_qpe.scales import eddington_mdot


ROOT = Path(__file__).resolve().parents[1]
TABLE_OUTPUT = ROOT / "outputs" / "tables" / "transonic_high_rate_ladder.md"
RESIDUAL_TOL = 3.0e-4
R_OUT_RG = 3000.0
SCOUT_N = 48
CONFIRM_N = 64
WARMUP_RATIOS = (1.0e-3, 2.5e-3, 5.0e-3, 1.0e-2, 2.0e-2, 3.0e-2)
SCOUT_RATIOS = (5.0e-2, 0.1, 0.15, 0.2, 0.3, 0.5, 0.7, 1.0, 1.5, 2.0)
CONFIRM_RATIOS = (0.3, 0.5, 1.0, 1.5, 2.0)
MIN_DLOG_RETRY = 0.035
WARMUP_SONIC_WEIGHTS = (1.0,)
TARGET_SONIC_WEIGHTS = (0.3, 1.0, 3.0)


def fmt(value: float) -> str:
    if not np.isfinite(value):
        return "nan"
    return f"{value:.4g}"


def dominant_block(audit) -> str:
    values = {
        "interval_R": abs(audit.interval_radial_max),
        "interval_E": abs(audit.interval_energy_max),
        "outer_Omega": abs(audit.outer_omega),
        "outer_E": abs(audit.outer_energy),
        "D": abs(audit.sonic_D),
        "C1": abs(audit.sonic_C1),
        "C2": abs(audit.sonic_C2),
    }
    return max(values, key=values.get)


def accepted_for_continuation(final) -> bool:
    status = final.status
    return bool(
        np.isfinite(final.max_residual)
        and final.max_residual <= RESIDUAL_TOL
        and status.optimizer_acceptable
        and status.equations_converged
        and status.sonic_regular
        and status.active_bounds_clear
        and status.positive_state
        and status.outer_thin
    )


def solve_one(
    *,
    M2_g: float,
    mdot_edd: float,
    alpha: float,
    ratio: float,
    n_nodes: int,
    R_out_rg: float,
    seed_profile,
    sonic_weight_sequence: tuple[float, ...],
):
    params = TransonicSlimParams(
        M2_g=M2_g,
        Mdot_g_s=ratio * mdot_edd,
        alpha=alpha,
        n_nodes=n_nodes,
        R_out_rg=R_out_rg,
        max_nfev=1800,
        residual_tol=RESIDUAL_TOL,
    )
    guess = remap_profile_to_new_sonic_grid(seed_profile, params) if seed_profile is not None else None
    result = solve_low_mdot_transonic_homotopy(
        params,
        initial_guess=guess,
        max_nfev_per_stage=900,
        final_max_nfev=1800,
        sonic_weight_sequence=sonic_weight_sequence,
    )
    return params, result


def row_from_result(label: str, ratio: float, n_nodes: int, R_out_rg: float, params, result, seed_label: str) -> dict[str, object]:
    final = result.final_result
    profile = final.profile
    audit = final.residual_audit
    status = final.status
    b2_stages = [stage for stage in result.stages if stage.name.startswith("B2_")]
    max_qadv_qvisc = float(np.max(np.abs(profile.Q_adv) / (np.abs(profile.Q_visc) + 1.0e-300)))
    max_xi = float(np.max(profile.xi_eff))
    accepted = accepted_for_continuation(final)
    return {
        "label": label,
        "seed_label": seed_label,
        "ratio": ratio,
        "n_nodes": n_nodes,
        "R_out_rg": R_out_rg,
        "accepted": accepted,
        "physical": status.physically_valid,
        "optimizer_ok": status.optimizer_acceptable,
        "equations": status.equations_converged,
        "sonic": status.sonic_regular,
        "outer_thin": status.outer_thin,
        "dominant": dominant_block(audit),
        "lambda_ratio": audit.lambda0_over_lK_isco,
        "Rson_rg": profile.sonic_radius / params.r_g,
        "max_HR": float(np.max(profile.H_over_R)),
        "outer_HR": audit.outer_H_over_R,
        "outer_Qadv_over_Qvisc": audit.outer_Qadv_over_Qvisc,
        "max_Qadv_over_Qvisc": max_qadv_qvisc,
        "integrated_advective_fraction": profile.integrated_advective_fraction,
        "max_xi": max_xi,
        "max_residual": final.max_residual,
        "interval_radial": audit.interval_radial_max,
        "interval_energy": audit.interval_energy_max,
        "outer_omega": audit.outer_omega,
        "outer_energy": audit.outer_energy,
        "sonic_D": audit.sonic_D,
        "sonic_C1": audit.sonic_C1,
        "sonic_C2": audit.sonic_C2,
        "stage_B2_last": b2_stages[-1].max_residual if b2_stages else np.nan,
        "nfev": final.nfev,
        "message": final.message,
    }


