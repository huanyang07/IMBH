def append_and_report(rows: list[dict[str, object]], row: dict[str, object]) -> None:
    rows.append(row)
    write_table(rows)
    print(
        f"{row['label']} N={row['n_nodes']} rate={row['ratio']:.4g} "
        f"accepted={row['accepted']} max={row['max_residual']:.4g} "
        f"H/R={row['max_HR']:.4g} adv={row['integrated_advective_fraction']:.4g} "
        f"dom={row['dominant']}",
        flush=True,
    )


def main() -> None:
    fiducial = FiducialParams()
    mdot_edd = eddington_mdot(fiducial.M2_g)
    rows: list[dict[str, object]] = []

    warmup_profile = None
    for ratio in WARMUP_RATIOS:
        params, result = solve_one(
            M2_g=fiducial.M2_g,
            mdot_edd=mdot_edd,
            alpha=fiducial.alpha_cool,
            ratio=ratio,
            n_nodes=24,
            R_out_rg=1000.0,
            seed_profile=warmup_profile,
            sonic_weight_sequence=WARMUP_SONIC_WEIGHTS,
        )
        row = row_from_result("warmup", ratio, 24, 1000.0, params, result, "previous")
        append_and_report(rows, row)
        if row["accepted"] or (result.final_result.status.sonic_regular and result.final_result.max_residual < 8.0e-4):
            warmup_profile = result.final_result.profile

    accepted_profiles: dict[float, object] = {}
    scout_profile = warmup_profile
    accepted_ratio: float | None = None
    pending = list(SCOUT_RATIOS)
    while pending:
        ratio = float(pending.pop(0))
        params, result = solve_one(
            M2_g=fiducial.M2_g,
            mdot_edd=mdot_edd,
            alpha=fiducial.alpha_cool,
            ratio=ratio,
            n_nodes=SCOUT_N,
            R_out_rg=R_OUT_RG,
            seed_profile=scout_profile,
            sonic_weight_sequence=TARGET_SONIC_WEIGHTS,
        )
        row = row_from_result("scout", ratio, SCOUT_N, R_OUT_RG, params, result, "accepted")
        append_and_report(rows, row)
        if row["accepted"]:
            scout_profile = result.final_result.profile
            accepted_profiles[ratio] = scout_profile
            accepted_ratio = ratio
            continue
        if accepted_ratio is None:
            break
        dlog = np.log(ratio / accepted_ratio)
        if dlog <= MIN_DLOG_RETRY:
            break
        midpoint = float(np.exp(0.5 * (np.log(ratio) + np.log(accepted_ratio))))
        if not pending or abs(pending[0] - midpoint) / midpoint > 1.0e-8:
            pending.insert(0, ratio)
            pending.insert(0, midpoint)

    confirm_profile = None
    for ratio in CONFIRM_RATIOS:
        if ratio not in accepted_profiles:
            continue
        params, result = solve_one(
            M2_g=fiducial.M2_g,
            mdot_edd=mdot_edd,
            alpha=fiducial.alpha_cool,
            ratio=ratio,
            n_nodes=CONFIRM_N,
            R_out_rg=R_OUT_RG,
            seed_profile=confirm_profile if confirm_profile is not None else accepted_profiles[ratio],
            sonic_weight_sequence=TARGET_SONIC_WEIGHTS,
        )
        row = row_from_result("confirm", ratio, CONFIRM_N, R_OUT_RG, params, result, "N48" if confirm_profile is None else "previous_N64")
        append_and_report(rows, row)
        if row["accepted"]:
            confirm_profile = result.final_result.profile

    print(f"wrote {TABLE_OUTPUT}")


if __name__ == "__main__":
    main()
